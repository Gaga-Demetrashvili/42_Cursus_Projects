#include "../builtins.h"
#include <stdlib.h>
#include <unistd.h>

static int	ft_atoi(const char *str)
{
	int	result;
	int	sign;

	result = 0;
	sign = 1;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		result = result * 10 + (*str - '0');
		str++;
	}
	return (result * sign);
}

int	builtin_exit(char **argv)
{
	int	exit_code;

	write(1, "exit\n", 5);
	if (argv[1])
		exit_code = ft_atoi(argv[1]);
	else
		exit_code = 0;
	exit(exit_code);
}
